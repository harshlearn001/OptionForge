from __future__ import annotations

from dataclasses import dataclass


@dataclass(
    frozen=True,
    slots=True,
)
class DealerPositionResult:
    """
    Institutional Dealer Position Result.

    This model contains both quantitative measurements and
    high-level interpretation generated by the Dealer
    Intelligence Engine.
    """

    # ======================================================
    # Quantitative Metrics
    # ======================================================

    dealer_position: float

    dealer_delta: float

    dealer_gamma: float

    net_exposure: float

    position_strength: float

    institutional_score: float

    # ======================================================
    # Classification
    # ======================================================

    dealer_bias: str

    dealer_direction: str

    market_condition: str

    market_stability: str

    directional_risk: str

    # ======================================================
    # Decision Support
    # ======================================================

    confidence: float

    recommendation: str

    interpretation: str